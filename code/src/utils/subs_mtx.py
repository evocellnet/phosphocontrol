def parse_blosum(blosum):
  """
  Return order and similarity scores from BLOSUM matrix

  order: dict of {res: idx_in_matrix}
  blosum_matrix: list hof lists with similarity scores
  """
  order = {}
  blosum_matrix = []
  for line in blosum.split('\n'):
    if line.startswith('#'):
      continue
    if not line.strip():
      continue
    parts = line.strip().split()
    if len(parts) == 24:
      for idx, sym in enumerate(parts):
        order[sym] = idx
    else:
      blosum_matrix.append(list(map(int, parts[1:])))

  blosum_dict = {}
  for k_i, v_i in order.items():
    for k_j, v_j in order.items():
      blosum_dict[(k_i, k_j)] = blosum_matrix[v_i][v_j]

  return blosum_dict


BLOSUM62_LOOKUP = parse_blosum(BLOSUM62)
